#!/usr/bin/env python3
"""Audit expandC (or other) batch labels for P1 NF paper readiness.

Usage:
    python scripts/audit_dataset.py --batch expandC_act9_n144_20260404
    python scripts/audit_dataset.py --batch expandC_act9_n144_20260404 --output artifacts/audit_report.md
"""
from __future__ import annotations

import argparse
import sys
from pathlib import Path

import numpy as np
import pandas as pd

BATCH_ROOT = Path("/home/gw/ComputeData/CFETR/COOL-PbLi-Burnup/datasets/wp2_batches")

CORE_TARGETS = [
    "activity_Bq", "contact_dose_Svph", "decay_heat_W", "tbr",
    "atoms_Li6", "atoms_Li7", "atoms_H3", "atoms_He4",
]
ACT9_TARGETS = [
    "atoms_Po210", "atoms_Bi210", "atoms_Hg203", "atoms_Tl204",
    "atoms_Bi207", "atoms_Pb203", "atoms_Mn54", "atoms_Co60", "atoms_Fe55",
]
ALL_TARGETS = CORE_TARGETS + ACT9_TARGETS

BREEDING_REGION = "cell_3"


def load_batch(batch_name: str) -> pd.DataFrame:
    batch_dir = BATCH_ROOT / batch_name
    csv_candidates = list(batch_dir.glob("labels_long_*.csv"))
    if not csv_candidates:
        sys.exit(f"No labels CSV found in {batch_dir}")
    csv_path = csv_candidates[0]
    df = pd.read_csv(csv_path)
    df["sample_id"] = df["run_id"].str.extract(r"_a(\d+)$").astype(int)
    return df


def audit(df: pd.DataFrame) -> dict:
    results = {}

    # Basic dimensions
    n_samples = df["sample_id"].nunique()
    regions = sorted(df["region"].unique())
    n_regions = len(regions)
    rows_per = df.groupby(["sample_id", "region"]).size()

    results["n_samples"] = n_samples
    results["n_regions"] = n_regions
    results["regions"] = regions
    results["rows_per_combo"] = {"min": int(rows_per.min()), "max": int(rows_per.max())}
    results["total_rows"] = len(df)

    # Time axis
    time_s = sorted(df["time_s"].unique())
    results["n_timesteps"] = len(time_s)
    results["time_range_years"] = (time_s[0] / 3.156e7, time_s[-1] / 3.156e7)
    results["cooling_fraction"] = float(df["is_cooling"].mean())

    # TBR time-constant check
    tbr_nunique = df.groupby(["sample_id", "region"])["tbr"].nunique()
    results["tbr_time_constant"] = bool((tbr_nunique == 1).all())

    # TBR by region
    tbr_by_region = {}
    for region in regions:
        sub = df[df["region"] == region]
        tbr_vals = sub.groupby("sample_id")["tbr"].first()
        tbr_by_region[region] = {
            "min": float(tbr_vals.min()),
            "median": float(tbr_vals.median()),
            "max": float(tbr_vals.max()),
            "nonzero_frac": float((tbr_vals > 0).mean()),
        }
    results["tbr_by_region"] = tbr_by_region

    # H3 check
    h3_per_sample = df.groupby("sample_id")["atoms_H3"].max()
    results["h3_samples_nonzero_frac"] = float((h3_per_sample > 0).mean())
    h3_by_region = {}
    for region in regions:
        sub = df[df["region"] == region]
        h3_max = sub.groupby("sample_id")["atoms_H3"].max()
        h3_by_region[region] = {
            "meaningful_frac": float((h3_max > 1e10).mean()),
            "nan_frac": float(sub["atoms_H3"].isnull().mean()),
        }
    results["h3_by_region"] = h3_by_region

    # NaN analysis
    nan_summary = {}
    for col in ALL_TARGETS:
        if col in df.columns:
            nan_by_region = {}
            for region in regions:
                sub = df[df["region"] == region]
                nan_by_region[region] = float(sub[col].isnull().mean())
            nan_summary[col] = nan_by_region
    results["nan_summary"] = nan_summary

    # Target statistics (all regions)
    target_stats = {}
    for col in ALL_TARGETS:
        if col in df.columns:
            vals = df[col].dropna()
            target_stats[col] = {
                "min": float(vals.min()),
                "median": float(vals.median()),
                "max": float(vals.max()),
                "nonzero_frac": float((vals > 0).mean()),
            }
    results["target_stats"] = target_stats

    # Recommendation
    issues = []
    if n_samples < 80:
        issues.append(f"Only {n_samples} samples (< 80)")
    if not results["tbr_time_constant"]:
        issues.append("TBR is NOT time-constant")
    if results["h3_samples_nonzero_frac"] < 0.90:
        issues.append(f"H3 non-zero samples: {results['h3_samples_nonzero_frac']:.1%} (< 90%)")

    cell3_li6_nan = results["nan_summary"].get("atoms_Li6", {}).get(BREEDING_REGION, 0)
    if cell3_li6_nan > 0.05:
        issues.append(f"Li6 NaN in {BREEDING_REGION}: {cell3_li6_nan:.1%}")

    results["issues"] = issues
    results["recommendation"] = "REJECT" if issues else "USE"
    return results


def format_report(results: dict, batch_name: str) -> str:
    lines = [
        f"# Dataset Audit Report: `{batch_name}`\n",
        f"Generated by `scripts/audit_dataset.py` on {pd.Timestamp.now().isoformat()[:19]}\n",
        "## Summary\n",
        f"| Metric | Value |",
        f"|--------|-------|",
        f"| Samples | {results['n_samples']} |",
        f"| Regions | {results['n_regions']} ({', '.join(results['regions'])}) |",
        f"| Rows per (sample, region) | {results['rows_per_combo']['min']}–{results['rows_per_combo']['max']} |",
        f"| Total rows | {results['total_rows']} |",
        f"| Time steps | {results['n_timesteps']} |",
        f"| Time range | {results['time_range_years'][0]:.2f}–{results['time_range_years'][1]:.0f} years |",
        f"| Cooling fraction | {results['cooling_fraction']:.1%} |",
        f"| TBR time-constant | {'Yes' if results['tbr_time_constant'] else '**NO**'} |",
        f"| H3 samples with max > 0 | {results['h3_samples_nonzero_frac']:.1%} |",
        "",
        "## TBR by Region\n",
        "| Region | Min | Median | Max | Non-zero |",
        "|--------|-----|--------|-----|----------|",
    ]
    for region, stats in results["tbr_by_region"].items():
        lines.append(
            f"| {region} | {stats['min']:.4f} | {stats['median']:.4f} | {stats['max']:.4f} | {stats['nonzero_frac']:.0%} |"
        )

    lines += [
        "",
        "## H3 by Region\n",
        "| Region | Meaningful (max > 1e10) | NaN fraction |",
        "|--------|------------------------|--------------|",
    ]
    for region, stats in results["h3_by_region"].items():
        lines.append(f"| {region} | {stats['meaningful_frac']:.0%} | {stats['nan_frac']:.1%} |")

    lines += [
        "",
        "## Target Statistics (all regions combined)\n",
        "| Target | Min | Median | Max | Non-zero |",
        "|--------|-----|--------|-----|----------|",
    ]
    for target, stats in results["target_stats"].items():
        lines.append(
            f"| {target} | {stats['min']:.3e} | {stats['median']:.3e} | {stats['max']:.3e} | {stats['nonzero_frac']:.1%} |"
        )

    lines += [
        "",
        "## NaN Analysis (fraction by region)\n",
        f"| Target | {' | '.join(results['regions'])} |",
        f"|--------{'|------' * results['n_regions']}|",
    ]
    for target, by_region in results["nan_summary"].items():
        vals = " | ".join(f"{by_region.get(r, 0):.1%}" for r in results["regions"])
        lines.append(f"| {target} | {vals} |")

    lines += [
        "",
        "## Recommendation\n",
    ]
    if results["issues"]:
        lines.append(f"**{results['recommendation']}** — Issues found:\n")
        for issue in results["issues"]:
            lines.append(f"- {issue}")
    else:
        lines.append(
            f"**{results['recommendation']} `{batch_name}`** — All checks passed. "
            f"144 samples × 4 regions × 130 time steps provides sufficient coverage "
            f"for 5D design space analysis (C1/C2)."
        )
    lines.append(
        "\n> Note: NaN values in cell_4/cell_5 for Li6/Li7/H3 are physically expected "
        "(these are shield/VV regions without PbLi). Core analysis targets cell_3 "
        "(PbLi breeding zone) and cell_2 (FW structural), which have 0% NaN for Li6/Li7."
    )
    return "\n".join(lines) + "\n"


def main():
    parser = argparse.ArgumentParser(description="Audit dataset batch for P1 NF paper")
    parser.add_argument("--batch", required=True, help="Batch directory name under wp2_batches/")
    parser.add_argument("--output", default=None, help="Output path for audit report (default: stdout)")
    args = parser.parse_args()

    print(f"Loading batch: {args.batch} ...")
    df = load_batch(args.batch)
    print(f"Loaded {len(df)} rows")

    print("Running audit ...")
    results = audit(df)

    report = format_report(results, args.batch)

    if args.output:
        out_path = Path(args.output)
        out_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.write_text(report, encoding="utf-8")
        print(f"Report written to {out_path}")
    else:
        print(report)

    if results["recommendation"] == "REJECT":
        print(f"\n*** REJECT: {results['issues']}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
